package dorres.spring.polymorphism.ex01;

public class LgTV {
	public void turnOn() {
		System.out.println("LgTV 전원을 켠다.");
	}

	public void turnOff() {
		System.out.println("LgTV 전원을 끈다.");
	}

	public void soundUp() {
		System.out.println("LgTV 볼륨을 올린다.");
	}

	public void soundDown() {
		System.out.println("LgTV 볼륨을 내린다.");
	}
}